package com.fortify.cli.common.json;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * This annotation is processed by GenerateActionSchema to append values from the
 * given enum class to the JSON property description.
 *
 * @author Ruud Senden
 */
@Retention(RUNTIME)
@Target(FIELD)
public @interface JsonPropertyDescriptionAppend {
    Class<? extends Enum<?>> value();
}
